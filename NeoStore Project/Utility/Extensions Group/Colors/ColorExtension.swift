import Foundation
import UIKit

extension UIColor {
    @nonobjc class var mainRed: UIColor {
        return UIColor(red: 233.0/255.0, green: 26.0/255.0, blue: 28.0/255.0, alpha: 1.0)
    }
    
    @nonobjc class var darkRed: UIColor {
        return UIColor(red: 158.0/255.0, green: 1.0/255.0, blue: 0.0/255.0, alpha: 1.0)
    }
}
